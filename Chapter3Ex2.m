%-a)-Show-that-the-Maximum-Likelihood-Estimator-of-lambda-(Poisson)-is-the-invere-of-the-Sample-Mean----------------------------
% Solution in Report.
%-------------------------------------------------------------------------------------------------------------------------------

%-b)-Call-expfun-for-different-combinations-of-M,-n,-lambda-and-check-if-x_bar=1/lambda-for-every-M-means-----------------------
lambda = 10:10:20;
l = length(lambda);
M = 10:10:20;
m = length(M);
n = 1000:1000:2000;
ns = length(n);
MMean = zeros(l*m*ns, 1);
counter = 1;
for i = 1:l
    for j = 1:m
        for k = 1:ns
            MMean(counter) = expfun(1/lambda(i), n(k), M(j));
            disp('-----------------------------------------------------------------------------------------------------------------------');
            fprintf('1/MMean = %f\n', 1/MMean(counter));
            fprintf('lambda = %f\n', lambda(i));
            counter = counter+1;
        end
    end
end
%------------------------------------------------------------------------------------------------------------------------------

%-Plots------------------------------------------------------------------------------------------------------------------------
disp('-----------------------------------------------------------------------------------------------------------------------');
disp('We notice that the Mean of M Means is almost equal to 1/lambda with an extremely small error. ');
disp('-----------------------------------------------------------------------------------------------------------------------');
