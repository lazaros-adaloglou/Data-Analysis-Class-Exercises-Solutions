%-b)-Call-poissonfun-for-different-combinations-of-M,-n,-lambda-and-check-if-x_bar=lambda-for-every-M-means---------------------
lambda = 10:10:20;
l = length(lambda);
M = 10:10:20;
m = length(M);
n = 1000:1000:2000;
ns = length(n);
MMean = zeros(length(lambda), length(M), length(n));
counter = 1;
for i = 1:l
    for j = 1:m
        for k = 1:ns
            MMean(counter) = poissonfun(lambda(i), M(j), n(k));
            disp('-----------------------------------------------------------------------------------------------------------------------');
            fprintf('MMean = %f\n', MMean(counter));
            fprintf('lambda = %f\n', lambda(i));
            counter = counter+1;
        end
    end
end
%-------------------------------------------------------------------------------------------------------------------------------

%-Plots------------------------------------------------------------------------------------------------------------------------
disp('-----------------------------------------------------------------------------------------------------------------------');
disp('We notice that the Mean of M Means is almost equal to lambda with an extremely small error. ');
disp('-----------------------------------------------------------------------------------------------------------------------');
