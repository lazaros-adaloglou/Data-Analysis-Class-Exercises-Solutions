%-Chapter-1-Exercise-5----------------------------------------------------------------------------------------------------------

%-Initialization----------------------------------------------------------------------------------------------------------------
format compact;
clc;
clear;
close all;
%-------------------------------------------------------------------------------------------------------------------------------

%-Calculate-mu-sigma-and-su-of-Normal-Distribution-[4, 0.01]--------------------------------------------------------------------
mu = 4;
sigma = 0.01;
su = sqrt(sigma);
%-------------------------------------------------------------------------------------------------------------------------------

%-P=P(X<=mu-su)=P(Z<=(mu-su-mu)/su)=P(Z<=-1)=PHI(-1)-(Z~N(0,1))-----------------------------------------------------------------
P = normcdf(-1, 0, 1);
%-------------------------------------------------------------------------------------------------------------------------------

%-P(X<=mu-e or X>=mu+e)=0.01->P(mu-e<=X<=mu+e)=0.99->P(-e/su<=Z<=e/su)=0.99->PHI(e/su)-PHI(-e/su)=0.99->2PHI(e/su)-1=0.99-------
%-PHI(e/su)=1.99/2->e/su=PHI^(-1)(1.99/2)---------------------------------------------------------------------------------------
epsilon = norminv(1.99/2, 0, 1)*su;
%-------------------------------------------------------------------------------------------------------------------------------

%-Plots-------------------------------------------------------------------------------------------------------------------------
disp('-----------------------------------------------------------------------------------------------------------------------');
fprintf('1) Probability of a steel bar breaking ---> %f\n', P);
disp('-----------------------------------------------------------------------------------------------------------------------');
fprintf('2) With Probability 0.99 the length of a steel bar will not diverge from mu more than ---> %f\n', epsilon);
disp('-----------------------------------------------------------------------------------------------------------------------');
