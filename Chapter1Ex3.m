%-Initialization----------------------------------------------------------------------------------------------------------------
format compact;
clc;
clear;
close all;
%-------------------------------------------------------------------------------------------------------------------------------

%-Calculate-Y-Random-Numbers-from-Exponential-Distribution----------------------------------------------------------------------
n = 1000;
mus = [0, 0];
sigmas = [3, 3];
R = mvnrnd(mus, sigmas, n);
X = R(:,1);
Y = R(:,2);
VarXY = var(X+Y);
VarX = var(X);
VarY = var(Y);
VarXplusY = VarX+VarY;
disp('-----------------------------------------------------------------------------------------------------------------------');
fprintf('Var[X+Y] ---> %f\n', VarXY);
fprintf('Var[X] + Var[Y] ---> %f\n', VarXplusY);
disp('-----------------------------------------------------------------------------------------------------------------------');
disp('Thus, Var[X+Y] =! Var[X] + Var[Y] when X and Y are not independent.');
disp('-----------------------------------------------------------------------------------------------------------------------');
%-------------------------------------------------------------------------------------------------------------------------------

%-Plots-------------------------------------------------------------------------------------------------------------------------
figure(1);
subplot(2, 1, 1);
hist(X);
title('X');
subplot(2, 1, 2);
hist(Y);
title('Y');
